import java.lang.*;
class relational_operator
{
public static void main(String args[])

{
int x = 10;
int y = 20;

if(x == y)
System.out.println("x == y");

if(x != y)
System.out.println("x != y");

if(x > y)
System.out.println("x > y");

if(x < y)
System.out.println("x < y");

if(x <= y)
System.out.println("x <= y");

}
}